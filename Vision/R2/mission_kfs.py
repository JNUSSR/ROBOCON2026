#!/usr/bin/env python3
"""
R2 比赛任务脚本 — KFS 梅花桩（收集 2 个 R2）
================================================
通过 KFS 规划器 (kfs_planner.py) 自动生成路径，转为 ROS2 指令执行。

┌─────────────────────────────────────────────────────────────┐
│  使用方式:                                                   │
│  1. 先启动行为树:  python3 ~/connect/r2_behavior_tree.py      │
│  2. 再运行本脚本:  python3 ~/connect/mission_kfs.py           │
│                                                             │
│  紧急停止: 按 Ctrl+C 会发送急停指令                            │
└─────────────────────────────────────────────────────────────┘
"""

from r2_actions import *
from kfs_planner import KFSPlanner


# ══════════════════════════════════════════════════════════════
#   ★★★  任务序列 —— 在这里编写你的比赛流程  ★★★
# ══════════════════════════════════════════════════════════════


def 执行任务序列():
    """
    ★ KFS 梅花桩 · 收集 2 个 R2 ★

    动作函数速查:
    ┌──────────────┬──────────────────────────────────┐
    │ 函数          │ 说明                             │
    ├──────────────┼──────────────────────────────────┤
    │ 急停()        │ 立即制动                         │
    │ 前进(d)       │ 前进 d 米 (负数=后退)            │
    │ 左移(d)       │ 左移 d 米 (负数=右移)            │
    │ 旋转(deg)     │ 旋转 deg° (正=左转, 负=右转)     │
    │ 组合移动(f,l,r)│ 前进f + 左移l + 旋转r°           │
    │ 机械臂(n)     │ 0=空闲 1=绘制KFS                 │
    │ 攀爬(n)       │ 0=初始 1=准备 2=上20 3=上40 4=下20│
    │ 夹爪(n)       │ 0=夹持角 1=闭合 2=复位 3=释放    │
    │ 下台阶(f,b)   │ 前进f→下降20cm→前进b (默认0.3,0.3) │
    │ 吸附墙()      │ 旋转到最近的墙方向 (0/±90/180°)   │
    │ 上台阶(f,c,b) │ 前进f→上爬c→前进b (默认0.3,2,0.3)│
    ├──────────────┼──────────────────────────────────┤
    │ 等待秒(s)     │ 等待 s 秒                        │
    │ 等待前进完成(d)│ 等待直线移动完成(+缓冲)          │
    │ 等待旋转完成(a)│ 等待旋转完成(+缓冲)              │
    │ 下台阶(f,b)   │ 前进f→下降20cm→前进b (默认0.2,0.3) │
    │ 等待吸附完成() │ 等待吸附墙完成（最长 ~7s）       │
    └──────────────┴──────────────────────────────────┘

    计时参考（含安全余量）:
      1.2m 前进≈ 7s      90° 旋转 ≈ 8s
      180° 旋转≈ 12s     上/下坡  ≈ 10s
    """
    print("=" * 60)
    print("  模板 1 — 崇武探幽全收集流程 启动")
    print(f"  开始时间: {_ts()}")
    print("=" * 60)

    # ── 初始化 ──
    急停()
    等待秒(0.5)
    攀爬(0)      # 初始 20cm 姿态
    等待秒(2.0)
    吸附墙()
    #等待吸附完成()
    前进(1.2)
    等待前进完成(1.2)
    旋转(-90)
    等待旋转完成(-90)
    前进(1.6)
    等待前进完成(1.6)
    旋转(90)
    等待旋转完成(90)
    前进(1.0)
    等待前进完成(1.0)
    机械臂(1)    # 伸出机械臂
    等待秒(2.0)
    # ── KFS 规划 ──
    planner = KFSPlanner()
    # ★★★ 手动设置 KFS 布局 ★★★
    # 坐标 (row, col): row=1靠入口 row=4出口侧, col=1左 col=2中 col=3右
    # 类型: 1=R1  2=R2  3=假方块  (未填的默认为空)
    planner.set_blocks({
        (4, 1): -1,  (4, 2): 1,  (4, 3): -1,
        (3, 1): -1, (3, 2): 2,  (3, 3): -1,
        (2, 1): -1, (2, 2): 2,  (2, 3): -1,
        (1, 1): -1, (1, 2): -1, (1, 3): 1,
    })
    planner.print_layout()
    print(f"\n[{_ts()}] 🧠 对抗区分析: {planner.analyze_confrontation()}")

    actions = planner.plan_collect_2_r2()
    if actions is None:
        print(f"\n[{_ts()}] ❌ 路径规划失败，任务中止！")
        急停()
        return

    print(f"\n[{_ts()}] 📋 规划成功，共 {len(actions)} 条动作指令:")
    for i, a in enumerate(actions):
        print(f"  {i+1}. [{a['type']}] {a['desc']}")

    # ── 执行动作序列 ──
    print(f"\n[{_ts()}] ▶ 开始执行动作序列...")
    for i, action in enumerate(actions):
        print(f"\n  [{_ts()}] 步骤 {i+1}/{len(actions)}: {action['desc']}")
        _execute_action(action)

    # ── 完成 ──
    攀爬(0)      # 恢复初始 20cm 姿态
    等待秒(2.0)
    print("\n" + "=" * 60)
    print(f"  ✅ 模板 1 全收集流程完成! 结束时间: {_ts()}")
    print("=" * 60)
    前进

# ══════════════════════════════════════════════════════════════
# 入口
# ══════════════════════════════════════════════════════════════

if __name__ == "__main__":
    print("""
╔══════════════════════════════════════════════════════════╗
║         R2 比赛任务脚本 — KFS 梅花桩（2 R2）                ║
║                                                          ║
║  前提: 行为树已启动 (python3 r2_behavior_tree.py)         ║
║  紧急: 按 Ctrl+C 急停                                    ║
╚══════════════════════════════════════════════════════════╝
""")
    input("按 Enter 开始执行任务序列...")
    执行任务序列()
